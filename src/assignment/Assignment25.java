package assignment;
//Write a program to print even & odd numbers from 0 to 100
public class Assignment25 {

	public static void main(String[] args) {
		  for(int i=1;i<=100;i++)
		  {
			  if(i%2==0)
			  {
				  System.out.println(i+" -->is even");
			  }
			  else
			  {
				  System.out.println(i+" -->is odd");
			  }
		  }

	}

}
